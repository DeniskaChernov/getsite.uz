<h2>Что должно быть в передаче</h2>
<p>Состояния кнопок, отступы, адаптив, тексты ошибок, пустые состояния. Без этого разработчик додумывает — и макет «плывёт». Handoff — это не «скинуть Figma», а набор решений.</p>
<p>Отдельно зафиксируйте, что можно упростить при вёрстке. Идеальная пиксельная точность не всегда стоит дней работы.</p>
