<h2>Когда бот выигрывает</h2>
<p>Аудитория уже в Telegram. Продукт простой: запись, заказ, FAQ. Не нужен большой каталог. Тогда бот запускается за 1–2 недели и закрывает задачу дешевле лендинга.</p>
<p>Сайт нужен, когда важны SEO, кейсы, доверие «с улицы» или каталог с фильтрами. Часто оптимальна связка: лендинг для доверия + бот для заявок.</p>
<figure class="article-figure">
  <img src="../assets/blog/bot-figure.svg" alt="Бот или сайт" width="1600" height="1000" loading="lazy">
  <figcaption>Часто лучший вариант — лендинг плюс бот, а не выбор «или-или».</figcaption>
