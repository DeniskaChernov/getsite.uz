<h2>Минимальный список перед публикацией</h2>
<ul>
  <li>Тестовая заявка с реального телефона доходит в нужный канал.</li>
  <li>Письма/уведомления не падают в спам и не уходят на битый адрес.</li>
  <li>Аналитика фиксирует визит, событие отправки формы и источник.</li>
  <li>Домен, SSL и редирект www/без www настроены без циклов.</li>
  <li>Ключевые экраны читаются на узком экране без горизонтального скролла.</li>
</ul>
<h2>Что чаще всего забывают в последний день</h2>
<p>Почта формы. SSL. Скорость hero-изображения. 404 на «спасибо». Ссылки в шапке, которые ведут в никуда после правок. Оставьте час только на эти пункты — это дешевле, чем чинить в панике под рекламой.</p>
<p>Если хотя бы один критический пункт красный — не запускайте трафик. Лучше сдвинуть релиз на день, чем платить за клики в сломанную воронку.</p>
