<h2>Три рабочих сценария под бота</h2>
<ol>
  <li>Запись на услугу с выбором слота и напоминанием.</li>
  <li>Заявка с квалификацией (бюджет/сроки/тип задачи).</li>
  <li>FAQ + эскалация живому менеджеру по кнопке.</li>
</ol>
<p>Если в сценарии появляются сложные сравнения товаров и десятки фильтров — лучше сайт. Бот начнёт превращаться в неудобный интерфейс.</p>
