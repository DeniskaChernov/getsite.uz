<h2>MVP — не половина большой системы</h2>
<p>MVP — сценарий, за который клиент готов платить или которым команда реально пользуется каждый день. Обычно это одно действие: оставить заявку, оформить заказ, увидеть статус. Всё «на будущее» уходит в бэклог.</p>
<p>Если в первый спринт входят и каталог, и личный кабинет, и сложная аналитика — это уже не MVP. Это сжатый «большой продукт», который сорвёт сроки.</p>
<figure class="article-figure">
  <img src="../assets/blog/mvp-figure.svg" alt="Границы MVP" width="1600" height="1000" loading="lazy">
  <figcaption>MVP — это один рабочий сценарий, а не «половина большой системы».</figcaption>
</figure>
<h2>Как резать scope без вреда</h2>
<ul>
  <li>Один главный сценарий пользователя — и только вокруг него UI.</li>
  <li>Интеграции: минимум для сигнала (Telegram/почта), остальное после живых данных.</li>
  <li>Админка: достаточно править контент вручную первые две недели.</li>
  <li>Дизайн-система: токены и сетка, без бесконечных вариаций карточек.</li>
</ul>
<h2>Срок 4–6 недель — когда реалистичен</h2>
<p>Когда цель ясная, контент готов вовремя, решений меньше десяти. И когда команда не переписывает бриф каждую неделю. Иначе срок плавает, а бюджет уходит на обсуждения.</p>
