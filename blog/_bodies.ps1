<h2>Статика, CMS или конструктор</h2>
<p>Статический HTML — быстрее всего, дёшевый хостинг, идеален для лендинга без регулярных правок от маркетинга. Headless CMS — если контент меняет не разработчик. Конструктор — ок для прототипа, но часто упирается в интеграции и скорость.</p>
<p>Выбирайте не по моде, а по тому, кто будет обновлять сайт через полгода и какие интеграции нужны в первый месяц.</p>
<figure class="article-figure">
  <img src="../assets/blog/stack-figure.svg" alt="Выбор стека" width="1600" height="1000" loading="lazy">
  <figcaption>Стек выбирают по поддержке через полгода, а не по моде.</figcaption>
</figure>
<h2>Короткая матрица решения</h2>
<ul>
  <li><strong>Лендинг + редкие правки</strong> — статика.</li>
  <li><strong>Частые тексты/акции без разработчика</strong> — CMS.</li>
  <li><strong>Проверка гипотезы за дни</strong> — конструктор, с планом миграции.</li>
</ul>
<p>Если уже ясны CRM, платежи и кастомная логика — закладывайте их в стек сразу. Перенос «потом» почти всегда дороже, чем кажется на старте.</p>
