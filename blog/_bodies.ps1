<h2>Проблема не в боте, а в контексте</h2>
<p>Типичный бот принимает телефон и кидает строку менеджеру. Дальше начинается игра в детектива: «а что человек хотел?». Хороший сценарий передаёт источник, интересующий продукт, последний шаг и короткий комментарий пользователя.</p>
<p>Тогда менеджер отвечает за один заход. Клиент чувствует, что его услышали. Конверсия из заявки в диалог растёт без увеличения рекламного бюджета.</p>
