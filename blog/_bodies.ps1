<h2>Минимум статусов, который уже спасает</h2>
<ol>
  <li><strong>Новая</strong> — ещё никто не взял.</li>
  <li><strong>В работе</strong> — менеджер на связи.</li>
  <li><strong>Закрыта</strong> — сделка или отказ с причиной.</li>
</ol>
